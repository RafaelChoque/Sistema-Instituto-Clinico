    private void tblPacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPacientesMouseClicked
        try {
            int fila = tblPacientes.getSelectedRow();
            if (fila == -1) {
                return;
            }

            int idPaciente = Integer.parseInt(tblPacientes.getValueAt(fila, 0).toString());

            Connection con = Conexion.obtenerConexion();
            PreparedStatement ps = con.prepareStatement(
                    "SELECT nombre, apellido, CI, telefono, fecha_nacimiento, numero_historial FROM pacientes WHERE id_paciente = ?"
            );
            ps.setInt(1, idPaciente);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                ID.setText(String.valueOf(idPaciente));
                Nombre.setText(rs.getString("nombre"));
                Apellido.setText(rs.getString("apellido"));
                CI.setText(rs.getString("CI"));
                Telefono.setText(rs.getString("telefono"));
                FechaNacimiento.setDate(rs.getDate("fecha_nacimiento"));
                txtNumeroHistorial.setText(String.valueOf(rs.getInt("numero_historial")));
            }

            rs.close();
            ps.close();
            con.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos del paciente: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_tblPacientesMouseClicked

