private void cargarTabla() {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                switch (columnIndex) {
                    case 0: 
                    case 6: 
                        return Integer.class;
                    case 5: 
                        return Date.class;
                    default:
                        return String.class;
                }
            }
        };
        modelo.setColumnIdentifiers(new Object[]{
            "ID", "Nombre", "Apellido", "CI", "Teléfono", "Fecha de Nacimiento", "Número de historial clínico"
        });

        try {
            Connection con = Conexion.obtenerConexion();
            PreparedStatement ps = con.prepareStatement(
                    "SELECT id_paciente, nombre, apellido, CI, telefono, fecha_nacimiento, numero_historial FROM pacientes"
            );
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id_paciente"), 
                    rs.getString("nombre"),
                    rs.getString("apellido"),
                    rs.getString("CI"),
                    rs.getString("telefono"),
                    rs.getDate("fecha_nacimiento"),
                    rs.getInt("numero_historial") 
                });
            }

            tblPacientes.setModel(modelo);


            sorter = new TableRowSorter<>(modelo);
            tblPacientes.setRowSorter(sorter);


            List<RowSorter.SortKey> sortKeys = new ArrayList<>();
            sortKeys.add(new RowSorter.SortKey(0, SortOrder.DESCENDING)); 
            sorter.setSortKeys(sortKeys);
            sorter.sort();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar los pacientes: " + ex.getMessage());
            ex.printStackTrace();
        }
    }


    public static String normalizar(String input) {
        if (input == null) {
            return null;
        }
        String normalized = Normalizer.normalize(input, Normalizer.Form.NFD);
        normalized = normalized.replaceAll("\\p{InCombiningDiacriticalMarks}+", "");
        return normalized.toLowerCase();
    }
    
    private void generarNumeroHistorial() {
        try {
            Connection con = Conexion.obtenerConexion();
            PreparedStatement ps = con.prepareStatement(
                    "SELECT IFNULL(MAX(numero_historial), 0) + 1 AS siguiente FROM pacientes"
            );
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int numeroHistorial = rs.getInt("siguiente");
                txtNumeroHistorial.setText(String.valueOf(numeroHistorial));
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al generar número de historial: " + ex.getMessage());
            ex.printStackTrace();
        }
    }

